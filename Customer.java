package CourseWork;

public class Customer {
	private String firstName;
	private String lastName;
	public void setFirstName(String n) {
		firstName=n;
	}
	public void setLastName(String n) {
		lastName=n;
	}
	public String getFirstName() {
		return firstName;
	}
	public String getLastName() {
		return lastName;
	}
}
